    private Beneficiary.Sex encodeSex(String sexString) {
        Beneficiary.Sex sex;
        
        if( sexString.compareTo("masculino") == 0 ) {
            sex = Beneficiary.Sex.MALE;
        }
        
        else {
            sex = Beneficiary.Sex.FEMALE;
        }   
        
        return sex;
    }     
